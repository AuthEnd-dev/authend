function pascalCase(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((segment) => segment.charAt(0).toUpperCase() + segment.slice(1))
    .join("");
}

function quoteDoc(value) {
  return String(value).replace(/\*\//g, "*\\/");
}

function typeForField(field) {
  let baseType = "string";
  switch (field.type) {
    case "boolean":
      baseType = "boolean";
      break;
    case "integer":
    case "bigint":
      baseType = "number";
      break;
    case "numeric":
      baseType = "string";
      break;
    case "jsonb":
      baseType = "unknown";
      break;
    case "enum":
      baseType =
        Array.isArray(field.enumValues) && field.enumValues.length > 0
          ? field.enumValues.map((value) => JSON.stringify(value)).join(" | ")
          : "string";
      break;
    default:
      baseType = "string";
      break;
  }

  return field.nullable ? `${baseType} | null` : baseType;
}

function renderDocBlock(lines) {
  if (!lines.length) {
    return [];
  }

  return [
    "/**",
    ...lines.map((line) => ` * ${quoteDoc(line)}`),
    " */",
  ];
}

function docLinesFromText(value) {
  if (!value) {
    return [];
  }

  return String(value)
    .split(/\r?\n/)
    .map((line) => line.trim())
    .filter(Boolean);
}

function fieldDocLines(field) {
  const lines = [
    ...docLinesFromText(field.description),
    `Type: ${field.type}.`,
    field.nullable ? "Nullable field." : "Required field.",
  ];

  if (field.default !== null && field.default !== undefined && String(field.default).trim().length > 0) {
    lines.push(`Default: ${String(field.default).trim()}.`);
  }

  if (Array.isArray(field.enumValues) && field.enumValues.length > 0) {
    lines.push(`Allowed values: ${field.enumValues.map((value) => JSON.stringify(value)).join(", ")}.`);
  }

  if (field.references) {
    lines.push(`References ${field.references.table}.${field.references.column}.`);
  }

  return lines;
}

function renderFieldsShape(fields, { partial = false } = {}) {
  if (!fields.length) {
    return null;
  }

  return [
    "{",
    ...fields.flatMap((field) => {
      const optional = partial || field.nullable || (field.default !== null && field.default !== undefined);
      return [
        ...renderDocBlock(fieldDocLines(field)),
        `  ${field.name}${optional ? "?" : ""}: ${typeForField(field)};`,
      ];
    }),
    "}",
  ].join("\n");
}

function renderUnion(values) {
  return values.length > 0 ? values.map((value) => JSON.stringify(value)).join(" | ") : "never";
}

function renderOperations(resource) {
  return [
    "{",
    `  list: ${resource.operations.list ? "true" : "false"};`,
    `  get: ${resource.operations.get ? "true" : "false"};`,
    `  create: ${resource.operations.create ? "true" : "false"};`,
    `  update: ${resource.operations.update ? "true" : "false"};`,
    `  delete: ${resource.operations.delete ? "true" : "false"};`,
    "}",
  ].join("\n");
}

export function generateSource(manifest, apiUrl) {
  const blocks = [
    "/* eslint-disable */",
    `// Generated by authend-gen from ${apiUrl}/api/system/sdk-schema`,
    `// Manifest version ${manifest.version}`,
    `// Generated at ${manifest.generatedAt}`,
    `// Schema checksum ${manifest.schemaChecksum}`,
    'import { defineAuthendSchema } from "@authend/sdk";',
    'import type { AuthendIncludeDefinition, AuthendSchemaResource, ResourceListParams } from "@authend/sdk";',
    "",
    `export const authendSchemaVersion = ${JSON.stringify(manifest.version)} as const;`,
    `export const authendSchemaChecksum = ${JSON.stringify(manifest.schemaChecksum)} as const;`,
    "",
  ];

  for (const resource of manifest.resources) {
    const typeBase = pascalCase(resource.key);
    const recordShape = renderFieldsShape(resource.fields);
    const createShape = resource.operations.create ? renderFieldsShape(resource.createFields) : null;
    const updateShape = resource.operations.update ? renderFieldsShape(resource.updateFields, { partial: true }) : null;
    const includeKeyUnion = renderUnion(resource.includeRelations.map((relation) => relation.key));
    const sortUnion = renderUnion(resource.sortFields);
    const filterUnion = renderUnion(resource.filterFields);

    blocks.push(...renderDocBlock([
      ...docLinesFromText(resource.description),
      `Table: ${resource.table}.`,
      `Route segment: ${resource.routeSegment}.`,
      `Auth mode: ${resource.authMode}.`,
    ]));
    blocks.push(`export interface ${typeBase}Record ${recordShape ?? "{}"}`);
    blocks.push("");
    blocks.push(...renderDocBlock([`Input payload for creating ${resource.displayName} records.`]));
    blocks.push(`export type ${typeBase}CreateInput = ${createShape ?? "never"};`);
    blocks.push("");
    blocks.push(...renderDocBlock([`Input payload for updating ${resource.displayName} records.`]));
    blocks.push(`export type ${typeBase}UpdateInput = ${updateShape ?? "never"};`);
    blocks.push("");
    blocks.push(`export type ${typeBase}SortField = ${sortUnion};`);
    blocks.push("");
    blocks.push(`export type ${typeBase}FilterField = ${filterUnion};`);
    blocks.push("");
    blocks.push(`export type ${typeBase}IncludeKey = ${includeKeyUnion};`);
    blocks.push("");
    if (resource.includeRelations.length > 0) {
      blocks.push(...renderDocBlock([`Available include relations for ${resource.displayName}.`]));
      blocks.push(`export interface ${typeBase}Includes {`);
      for (const relation of resource.includeRelations) {
        const targetBase = pascalCase(relation.targetKey);
        blocks.push(...renderDocBlock([`Include ${relation.key} from ${relation.targetTable}.`]).map((line) => `  ${line}`));
        blocks.push(
          `  ${relation.key}: AuthendIncludeDefinition<${targetBase}Record, ${JSON.stringify(relation.resultKey)}>;`,
        );
      }
      blocks.push("}");
    } else {
      blocks.push(`export type ${typeBase}Includes = {};`);
    }
    blocks.push("");
    blocks.push(
      `export type ${typeBase}ListParams = ResourceListParams<${typeBase}SortField, ${typeBase}FilterField, ${typeBase}IncludeKey>;`,
    );
    blocks.push("");
  }

  blocks.push("export interface AuthendGeneratedSchema {");
  blocks.push("  resources: {");
  for (const resource of manifest.resources) {
    const typeBase = pascalCase(resource.key);
    blocks.push(
      `    ${resource.key}: AuthendSchemaResource<${typeBase}Record, ${typeBase}CreateInput, ${typeBase}UpdateInput, ${typeBase}SortField, ${typeBase}FilterField, ${typeBase}Includes, ${renderOperations(resource)}>;`,
    );
  }
  blocks.push("  };");
  blocks.push("}");
  blocks.push("");
  blocks.push("export const authendSchema = defineAuthendSchema<AuthendGeneratedSchema>({");
  blocks.push("  resources: {");
  for (const resource of manifest.resources) {
    blocks.push(`    ${resource.key}: {`);
    blocks.push(`      routeSegment: ${JSON.stringify(resource.routeSegment)},`);
    blocks.push("      operations: {");
    blocks.push(`        list: ${resource.operations.list ? "true" : "false"},`);
    blocks.push(`        get: ${resource.operations.get ? "true" : "false"},`);
    blocks.push(`        create: ${resource.operations.create ? "true" : "false"},`);
    blocks.push(`        update: ${resource.operations.update ? "true" : "false"},`);
    blocks.push(`        delete: ${resource.operations.delete ? "true" : "false"},`);
    blocks.push("      },");
    blocks.push("    },");
  }
  blocks.push("  },");
  blocks.push("});");
  blocks.push("");
  blocks.push("export type AuthendSchema = AuthendGeneratedSchema;");
  blocks.push("");

  return blocks.join("\n");
}

export { pascalCase, typeForField };
